function __transliteration_online() {
    static $online = null;
    if ($online !== null) {
        return $online;
    }
    $socket = @fsockopen('ssl://inputtools.google.com', 443, $errno, $errstr, 0.4);
    $online = $socket !== false;
    if ($online) {
        fclose($socket);
    }
    return $online;
}

function __transliterate($text, $lang_code) {
    $map = [
        'hi' => 'hi-t-i0-und',
        'kn' => 'kn-t-i0-und',
        'te' => 'te-t-i0-und',
        'ta' => 'ta-t-i0-und',
        'ml' => 'ml-t-i0-und',
        'mr' => 'mr-t-i0-und',
        'gu' => 'gu-t-i0-und',
        'bn' => 'bn-t-i0-und',
        'ar' => 'ar-t-i0-und'
    ];
    
    if (!isset($map[$lang_code])) {
        return $text;
    }

    if (!__transliteration_online()) {
        return $text;
    }
    
    $cache_key = "translit_" . $lang_code . "_" . md5($text);
    if (isset($_SESSION[$cache_key])) {
        return $_SESSION[$cache_key];
    }
    
    $itc = $map[$lang_code];
    $url = "https://inputtools.google.com/request?text=" . urlencode($text) . "&itc=" . $itc . "&num=1";
    
    $ctx = stream_context_create([
        'http' => ['timeout' => 1.0]
    ]);
    
    try {
        $res = @file_get_contents($url, false, $ctx);
        if ($res) {
            $data = json_decode($res, true);
            if ($data && isset($data[0]) && $data[0] === 'SUCCESS') {
                $candidates = $data[1][0][1];
                if (isset($candidates[0])) {
                    $transliterated = $candidates[0];
                    $_SESSION[$cache_key] = $transliterated;
                    return $transliterated;
                }
            }
        }
    } catch (Exception $e) {
        // Fallback silently
    }
    
    return $text;
}
